<template>
  <HeaderContainer :title="projName">
    <template #menu-content>
      <!--渲染 menuStore.menuList 内容-->
      <el-menu
        :default-active="activeKey"
        :ellipsis="false"
        mode="horizontal"
        @select="onMenuSelect"
      >
        <template v-for="item in menuStore.menuList">
          <SubMenu
            v-if="item.subMenu && item.subMenu.length > 0"
            :menuItem="item"
            :key="item.key"
          ></SubMenu>
          <el-menu-item
            v-else
            :index="item.key"
          >{{ item.name }}</el-menu-item>
        </template>
      </el-menu>
    </template>
    <template #setting-content>
      <!--渲染 projectStore.projectList 内容-->
      <el-dropdown @command="handleProjectCommand">
        <span class="project-list">
          {{ projName }}
          <el-icon v-if="projectStore.projectList.length > 1" class="el-icon-right">
            <ArrowDown />
          </el-icon>
        </span>
        <template v-if="projectStore.projectList.length > 1" #dropdown>
          <el-dropdown-menu >
            <el-dropdown-item
              v-for="item in projectStore.projectList"
              :key="item.key"
              :command="item.key"
              :disabled="item.name === projName"
            >{{ item.name }}</el-dropdown-item>
          </el-dropdown-menu>
        </template>
      </el-dropdown>
    </template>
    <template #main-content>
      <slot name="main-content"></slot>
    </template>
  </HeaderContainer>
</template>

<script setup>
import { ref, onMounted, watch } from 'vue';
import { useRoute } from 'vue-router';
import HeaderContainer from '$elpisWidgets/header-container/header-container';
import SubMenu from './complex-view/sub-menu.vue';
import { useMenuStore } from '$elpisPages/store/menu';
import { useProjectStore } from '$elpisPages/store/project';
import { ArrowDown } from '@element-plus/icons-vue';

defineProps({
    projName: String
});
const emit = defineEmits(['menu-select']);
const activeKey = ref('');
const menuStore = useMenuStore();
const projectStore = useProjectStore();
const route = useRoute();

watch(() => route.query.key, setActiveKey);
watch(() => menuStore.menuList, setActiveKey);
onMounted(setActiveKey);

function setActiveKey () {
    const menuItem = menuStore.findMenuItem({
        key: 'key',
        value: route.query.key
    });

    activeKey.value = Array.isArray(menuItem?.subMenu) ? menuItem.subMenu[0]?.key : menuItem?.key;
}

const onMenuSelect = (menuKey) => {
    const menuItem = menuStore.findMenuItem({
        key: 'key',
        value: menuKey
    });
    emit('menu-select', menuItem);
};
const handleProjectCommand = (projKey) => {
    const projItem = projectStore.projectList.find(item => item.key === projKey);
    if (!projItem || !projItem.homePage) {
        return;
    }
    const { host } = window.location;
    window.location.replace(`http://${host}/view/dashboard${projItem.homePage}`);
};
</script>

<style lang="less" scoped>
.project-list{
  margin-right: 20px;
  cursor: pointer;
  color: var(--el-color-primary);
  display: flex;
  align-items: center;
  outline: none;
}
:deep(.el-menu--horizontal.el-menu) {
  border-bottom: 0;
}
</style>